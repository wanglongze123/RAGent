# 构建反向索引：词 → 扩展词列表
_EXPANSION_MAP: dict[str, list[str]] = {}
for canonical, synonyms in SYNONYM_DICT.items():
    for syn in synonyms:
        if syn not in _EXPANSION_MAP:
            _EXPANSION_MAP[syn] = []
        for s in synonyms:
            if s not in _EXPANSION_MAP[syn]:
                _EXPANSION_MAP[syn].append(s)


def expand_query(query: str) -> str:
    """
    把 query 里命中同义词词典的词替换为扩展形式。
    例："推荐适合油皮的洗面奶" → "推荐适合油皮的洗面奶 洁面乳 洁面 洗颜"
    """
    expansions: list[str] = []
    for term, expanded in _EXPANSION_MAP.items():
        if term in query:
            for syn in expanded:
                if syn != term and syn not in query:
                    expansions.append(syn)
    if expansions:
        return query + " " + " ".join(expansions)
    return query
